export interface NewsItem {
  id: string | number;
  category: string;
  categoryEn: string;
  categorySlug: string;
  title: string;
  titleEn: string;
  summary: string;
  summaryEn: string;
  image: string;
  time: string;
  timeEn: string;
  author: string;
  authorEn: string;
  isBreaking?: boolean;
  content?: string[];
  contentEn?: string[];
  tags?: string[];
  tagsEn?: string[];
  readTime?: string;
  /** From CMS when available */
  viewCount?: number;
}

export interface VideoItem {
  id: string | number;
  title: string;
  titleEn: string;
  /** Legacy / fallback image when URL has no YouTube id */
  thumbnail: string;
  duration: string;
  views: string;
  category: string;
  categoryEn: string;
  youtubeUrl: string;
  /** Short dek under the headline (shown on home video rail + shows) */
  summary?: string;
  summaryEn?: string;
  /** Optional upload line e.g. "2 दिन पहले" */
  publishedHi?: string;
  publishedEn?: string;
}

export interface OpinionItem extends NewsItem {
  opinionType: "analysis" | "opinion" | "column" | "ground-report";
  authorDesignation: string;
  authorDesignationEn: string;
  authorInitials: string;
}

export interface FactCheckItem {
  id: number;
  verdict: "true" | "false" | "misleading" | "partly-true";
  claim: string;
  claimEn: string;
  explanation: string;
  explanationEn: string;
  source: string;
}

export interface PodcastItem {
  id: number;
  title: string;
  titleEn: string;
  series: string;
  seriesEn: string;
  guest: string;
  guestEn: string;
  duration: string;
  episodeNumber: number;
  artwork: string;
  publishedHi: string;
  publishedEn: string;
}

export interface PhotoItem {
  id: number;
  image: string;
  caption: string;
  captionEn: string;
  photographer: string;
  category: string;
  categoryEn: string;
  isFeatured?: boolean;
}

